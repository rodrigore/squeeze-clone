function love.keypressed(key)
  if key == "q" then love.event.quit() end

  if key == "down" then
    player.y = player.y + 24
  elseif key == "up" then
    player.y = player.y - 24
  elseif key == "left" then
    player.x = player.x - 24
  elseif key == "right" then
    player.x = player.x + 24
  end
end

function love.draw()
  -- debug
  local statistics = ("fps: %d\n mem: %dKB\n x: %d, y: %d\n %dx%d"):format(love.timer.getFPS(), collectgarbage("count"), player.x, player.y, love.graphics.getDimensions())
  love.graphics.printf(statistics, 620, 10, 200, 'right')

  -- player
  love.graphics.rectangle("fill", player.x, player.y, player.width, player.height)

  -- bricks
  for y =1, #map do
    for x=1, #map[y] do
      if map[y][x] == 1 then
        love.graphics.rectangle("line", (x - 1) * 24, (y -1) * 24, 24, 24)
      end
    end
  end
end

function generateMap()
  map = {}

  for i = 1, 20 do
    for j = 1, 30 do
      if (j == 1) then
        map[i] = {}
      end
      map[i][j] = math.random(0, 1) * math.random(0, 1)
    end
  end

  return map
end
